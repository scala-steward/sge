    */
  def setOnlyFontChars(onlyFontChars: Boolean): Unit =
    this.onlyFontChars = onlyFontChars

  def setStyle(style: TextField.TextFieldStyle): Unit = {
    this._style = style

    textHeight = style.font.capHeight - style.font.descent * 2
    updateDisplayText()
    invalidateHierarchy()
  }

  /** Returns the text field's style. Modifying the returned style may not have an effect until {@link #setStyle(TextFieldStyle)} is called.
    */
  def getStyle: TextField.TextFieldStyle = _style

  protected def calculateOffsets(): Unit = {
    var visibleWidth = this.width
    val background   = getBackgroundDrawable()
    background.foreach { bg =>
      visibleWidth -= bg.leftWidth + bg.rightWidth
    }

    val glyphCount = glyphPositions.size

    // Check if the cursor has gone out the left or right side of the visible area and adjust renderOffset.
    cursor = MathUtils.clamp(cursor, 0, glyphCount - 1)
    val distance = glyphPositions(Math.max(0, cursor - 1)) + renderOffset
    if (distance <= 0)
      renderOffset -= distance
    else {
      val index = Math.min(glyphCount - 1, cursor + 1)
      val minX  = glyphPositions(index) - visibleWidth
      if (-renderOffset < minX) renderOffset = -minX
    }

    // Prevent renderOffset from starting too close to the end, eg after text was deleted.
    var maxOffset = 0f
    val width     = glyphPositions(glyphCount - 1)
    var i         = glyphCount - 2
    boundary {
      while (i >= 0) {
        val x = glyphPositions(i)
        if (width - x > visibleWidth) boundary.break(())
        maxOffset = x
        i -= 1
      }
    }
    if (-renderOffset > maxOffset) renderOffset = -maxOffset

    // calculate first visible char based on render offset
    visibleTextStart = 0
    var startX = 0f
    i = 0
    boundary {
      while (i < glyphCount) {
        if (glyphPositions(i) >= -renderOffset) {
          visibleTextStart = i
          startX = glyphPositions(i)
          boundary.break(())
        }
        i += 1
      }
    }

    // calculate last visible char based on visible width and render offset
    var end  = visibleTextStart + 1
    val endX = visibleWidth - renderOffset
    val n    = Math.min(displayText.length(), glyphCount)
    boundary {
      while (end <= n) {
        if (glyphPositions(end) > endX) boundary.break(())
        end += 1
      }
    }
    visibleTextEnd = Math.max(0, end - 1)

    if ((textHAlign & Align.left).toInt == 0) {
      textOffset = visibleWidth - glyphPositions(visibleTextEnd) - fontOffset + startX
      if ((textHAlign & Align.center).toInt != 0) textOffset = Math.round(textOffset * 0.5f).toFloat
    } else {
      textOffset = startX + renderOffset
    }

    // calculate selection x position and width
    if (hasSelection) {
      val minIndex = Math.min(cursor, selectionStart)
      val maxIndex = Math.max(cursor, selectionStart)
      val minX     = Math.max(glyphPositions(minIndex) - glyphPositions(visibleTextStart), -textOffset)
      val maxX     = Math.min(glyphPositions(maxIndex) - glyphPositions(visibleTextStart), visibleWidth - textOffset)
      selectionX = minX
      selectionWidth = maxX - minX - _style.font.data.cursorX
    }
  }

  protected def getBackgroundDrawable(): Nullable[Drawable] =
    if (disabled && _style.disabledBackground.isDefined) _style.disabledBackground
    else if (_style.focusedBackground.isDefined && hasKeyboardFocus) _style.focusedBackground
    else _style.background

  override def draw(batch: Batch, parentAlpha: Float): Unit = {
    val focused = hasKeyboardFocus
    if (focused != this.focused || (focused && !blinkTask.isScheduled)) {
      this.focused = focused
      blinkTask.cancel()
      cursorOn = focused
      if (focused)
        Timer.schedule(blinkTask, blinkTime, blinkTime)
      else
        keyRepeatTask.cancel()
    } else if (!focused) {
      cursorOn = false
    }

    val font = _style.font
    val fontColor: Color =
      if (disabled && _style.disabledFontColor.isDefined) _style.disabledFontColor.getOrElse(_style.fontColor)
      else if (focused && _style.focusedFontColor.isDefined) _style.focusedFontColor.getOrElse(_style.fontColor)
      else _style.fontColor
    val selection   = _style.selection
    val cursorPatch = _style.cursor
    val background  = getBackgroundDrawable()

    val color = this.color
    val tx    = this.x
    val ty    = this.y
    val tw    = this.width
    val th    = this.height

    batch.setColor(color.r, color.g, color.b, color.a * parentAlpha)
    var bgLeftWidth  = 0f
    var bgRightWidth = 0f
    background.foreach { bg =>
      drawBackground(bg, batch, tx, ty, tw, th)
      bgLeftWidth = bg.leftWidth
      bgRightWidth = bg.rightWidth
    }

    val textY = getTextY(font, background)
    calculateOffsets()

    if (focused && hasSelection) {
      selection.foreach { sel =>
        drawSelection(sel, batch, font, tx + bgLeftWidth, ty + textY)
      }
    }

    val yOffset = if (font.flipped) -textHeight else 0
    if (displayText.length() == 0) {
      if ((!focused || disabled) && messageText.isDefined) {
        val messageFont = _style.messageFont.getOrElse(font)
        _style.messageFontColor.fold {
          messageFont.setColor(0.7f, 0.7f, 0.7f, color.a * parentAlpha)
        } { mfc =>
          messageFont.setColor(mfc.r, mfc.g, mfc.b, mfc.a * color.a * parentAlpha)
        }
        drawMessageText(batch, messageFont, tx + bgLeftWidth, ty + textY + yOffset, tw - bgLeftWidth - bgRightWidth)
      }
    } else {
      val data          = font.data
      val markupEnabled = data.markupEnabled
      data.markupEnabled = false
      font.setColor(fontColor.r, fontColor.g, fontColor.b, fontColor.a * color.a * parentAlpha)
      drawText(batch, font, tx + bgLeftWidth, ty + textY + yOffset)
      data.markupEnabled = markupEnabled
    }
    if (!disabled && cursorOn) {
      cursorPatch.foreach { cp =>
        drawCursor(cp, batch, font, tx + bgLeftWidth, ty + textY)
      }
    }
  }

  protected def getTextY(font: BitmapFont, background: Nullable[Drawable]): Float = {
    val height = this.height
    var textY  = textHeight / 2 + font.descent
    background.fold {
      textY = textY + height / 2
    } { bg =>
      val bottom = bg.bottomHeight
      textY = textY + (height - bg.topHeight - bottom) / 2 + bottom
    }
    if (font.integerPositions) textY = textY.toInt.toFloat
    textY
  }

  protected def drawBackground(background: Drawable, batch: Batch, x: Float, y: Float, width: Float, height: Float): Unit =
    background.draw(batch, x, y, width, height)

  /** Draws selection rectangle * */
  protected def drawSelection(selection: Drawable, batch: Batch, font: BitmapFont, x: Float, y: Float): Unit =
    selection.draw(batch, x + textOffset + selectionX + fontOffset, y - textHeight - font.descent, selectionWidth, textHeight)

  protected def drawText(batch: Batch, font: BitmapFont, x: Float, y: Float): Unit =
    font.draw(batch, displayText, x + textOffset, y, visibleTextStart, visibleTextEnd, 0, Align.left.toInt, false)

  protected def drawMessageText(batch: Batch, font: BitmapFont, x: Float, y: Float, maxWidth: Float): Unit =
    messageText.foreach { msg =>
      font.draw(batch, msg, x, y, 0, msg.length(), maxWidth, textHAlign.toInt, false, Nullable("..."))
    }

  protected def drawCursor(cursorPatch: Drawable, batch: Batch, font: BitmapFont, x: Float, y: Float): Unit =
    cursorPatch.draw(
      batch,
      x + textOffset + glyphPositions(cursor) - glyphPositions(visibleTextStart) + fontOffset + font.data.cursorX,
      y - textHeight - font.descent,
      cursorPatch.minWidth,
      textHeight
    )

  protected def updateDisplayText(): Unit = {
    val font       = _style.font
    val data       = font.data
    val text       = this._text
    val textLength = text.length()

    val buffer = new java.lang.StringBuilder()
    var i      = 0
    while (i < textLength) {
      val c = text.charAt(i)
      buffer.append(if (data.hasGlyph(c)) c else ' ')
      i += 1
    }
    val newDisplayText = buffer.toString

    if (passwordMode && data.hasGlyph(passwordCharacter)) {
      val pwBuf = passwordBuffer.getOrElse {
        val sb = new java.lang.StringBuilder(newDisplayText.length())
        passwordBuffer = Nullable(sb)
        sb
      }
      if (pwBuf.length() > textLength)
        pwBuf.setLength(textLength)
      else {
        var j = pwBuf.length()
        while (j < textLength) {
          pwBuf.append(passwordCharacter)
          j += 1
        }
      }
      displayText = pwBuf
    } else {
      displayText = newDisplayText
    }

    val markupEnabled = data.markupEnabled
    data.markupEnabled = false
    glyphLayout.setText(font, displayText.toString.replace('\r', ' ').replace('\n', ' '))
    data.markupEnabled = markupEnabled

    glyphPositions.clear()
    var x = 0f
    if (glyphLayout.runs.nonEmpty) {
      val run       = glyphLayout.runs.first
      val xAdvances = run.xAdvances
      fontOffset = xAdvances.first
      var idx = 1
      while (idx < xAdvances.size) {
        glyphPositions.add(x)
        x += xAdvances(idx)
        idx += 1
      }
    } else {
      fontOffset = 0
    }
    glyphPositions.add(x)

    visibleTextStart = Math.min(visibleTextStart, glyphPositions.size - 1)
    visibleTextEnd = MathUtils.clamp(visibleTextEnd, visibleTextStart, glyphPositions.size - 1)

    if (selectionStart > newDisplayText.length()) selectionStart = textLength
  }

  /** Copies the contents of this TextField to the {@link Clipboard} implementation set on this TextField. */
  def copy(): Unit =
    if (hasSelection && !passwordMode) {
      clipboard.contents = Nullable(_text.substring(Math.min(cursor, selectionStart), Math.max(cursor, selectionStart)))
    }

  /** Copies the selected contents of this TextField to the {@link Clipboard} implementation set on this TextField, then removes it.
    */
  def cut(): Unit =
    cut(programmaticChangeEvents)

  private[ui] def cut(fireChangeEvent: Boolean): Unit =
    if (hasSelection && !passwordMode) {
      copy()
      cursor = delete(fireChangeEvent)
      updateDisplayText()
    }

  private[ui] def paste(content: Nullable[String], fireChangeEvent: Boolean): Unit =
    content.foreach { rawContent =>
      val buffer     = new java.lang.StringBuilder()
      var textLength = _text.length()
      if (hasSelection) textLength -= Math.abs(cursor - selectionStart)
      val data = _style.font.data
      var i    = 0
      val n    = rawContent.length()
      boundary {
        while (i < n) {
          if (!withinMaxLength(textLength + buffer.length())) boundary.break(())
          val c = rawContent.charAt(i)
          if (!(writeEnters && (c == NEWLINE || c == CARRIAGE_RETURN))) {
            if (c == '\r' || c == '\n') { i += 1 }
            else if (onlyFontChars && !data.hasGlyph(c)) { i += 1 }
            else {
              var accepted = true
              filter.foreach { f =>
                if (!f.acceptChar(this, c)) accepted = false
              }
              if (accepted) {
                buffer.append(c)
              }
              i += 1
            }
          } else {
            buffer.append(c)
            i += 1
          }
        }
      }
      val processedContent = buffer.toString

      if (hasSelection) cursor = delete(fireChangeEvent)
      if (fireChangeEvent)
        changeText(_text, insert(cursor, processedContent, _text))
      else
        _text = insert(cursor, processedContent, _text)
      updateDisplayText()
      cursor += processedContent.length()
    }

  private[ui] def insert(position: Int, text: CharSequence, to: String): String =
    if (to.length() == 0) text.toString
    else to.substring(0, position) + text + to.substring(position, to.length())

  private[ui] def delete(fireChangeEvent: Boolean): Int = {
    val from     = selectionStart
    val to       = cursor
    val minIndex = Math.min(from, to)
    val maxIndex = Math.max(from, to)
    val newText  = (if (minIndex > 0) _text.substring(0, minIndex) else "") +
      (if (maxIndex < _text.length()) _text.substring(maxIndex, _text.length()) else "")
    if (fireChangeEvent)
      changeText(_text, newText)
    else
      _text = newText
    clearSelection()
    minIndex
  }

  /** Sets the {@link Stage#setKeyboardFocus(Actor) keyboard focus} to the next TextField. If no next text field is found, the onscreen keyboard is hidden. Does nothing if the text field is not in a
    * stage.
    * @param up
    *   If true, the text field with the same or next smallest y coordinate is found, else the next highest.
    * @return
    *   The textfield, the focus has been transferred to
    */
  def next(up: Boolean): Nullable[TextField] = boundary {
    if (this.stage.isEmpty) boundary.break(Nullable.empty)
    val stg = this.stage.getOrElse(throw new IllegalStateException("TextField must be on a stage"))
    var current: TextField = this
    val currentCoords = current.getParent.getOrElse(throw new IllegalStateException("TextField must have a parent")).localToStageCoordinates(tmp2.set(current.x, current.y))
    val bestCoords    = tmp1
    val continue      = true
    while (continue) {
      var textField = current.findNextTextField(stg.getActors, Nullable.empty, bestCoords, currentCoords, up)
      if (textField.isEmpty) { // Try to wrap around.
        if (up)
          currentCoords.set(-Float.MaxValue, -Float.MaxValue)
        else
          currentCoords.set(Float.MaxValue, Float.MaxValue)
        textField = current.findNextTextField(stg.getActors, Nullable.empty, bestCoords, currentCoords, up)
      }
      if (textField.isEmpty) {
        boundary.break(Nullable.empty)
      }
      val tf = textField.getOrElse(throw new IllegalStateException("textField must be defined"))
      if (stg.setKeyboardFocus(Nullable(tf))) {
        tf.selectAll()
        boundary.break(Nullable(tf))
      }
      current = tf
      currentCoords.set(bestCoords)
    }
    Nullable.empty // unreachable but needed for type
  }

  /** @return May be null. */
  private def findNextTextField(actors: DynamicArray[Actor], best: Nullable[TextField], bestCoords: Vector2, currentCoords: Vector2, up: Boolean): Nullable[TextField] = boundary {
    var currentBest = best
    var i           = 0
    val n           = actors.size
    while (i < n) {
      val actor = actors(i)
      actor match {
        case textField: TextField if !(textField eq this) =>
          if (!(textField.isDisabled || !textField.focusTraversal || !textField.ascendantsVisible())) {
            val actorCoords = actor.getParent.getOrElse(throw new IllegalStateException("Actor must have a parent")).localToStageCoordinates(tmp3.set(actor.x, actor.y))
            val below       = actorCoords.y != currentCoords.y && (actorCoords.y < currentCoords.y ^ up)
            val right       = actorCoords.y == currentCoords.y && (actorCoords.x > currentCoords.x ^ up)
            if (below || right) {
              var better = currentBest.isEmpty || (actorCoords.y != bestCoords.y && (actorCoords.y > bestCoords.y ^ up))
              if (!better) better = actorCoords.y == bestCoords.y && (actorCoords.x < bestCoords.x ^ up)
              if (better) {
                currentBest = Nullable(textField)
                bestCoords.set(actorCoords)
              }
            }
          }
        case group: Group =>
          currentBest = findNextTextField(group.getChildren, currentBest, bestCoords, currentCoords, up)
        case _ =>
      }
      i += 1
    }
    currentBest
  }

