  var cursorOn:  Boolean    = false
  var blinkTime: Seconds    = Seconds(0.32f)
  val blinkTask: Timer.Task = new Timer.Task {
    def run(): Unit =
      if (stage.isEmpty) {
        cancel()
      } else {
        cursorOn = !cursorOn
        Sge().graphics.requestRendering()
      }
  }

  val keyRepeatTask:            KeyRepeatTask = KeyRepeatTask()
  var programmaticChangeEvents: Boolean       = false

  setStyle(style)
