  /** @param str If null, "" is used. */
  def appendText(str: Nullable[String]): Unit = {
    val s = str.getOrElse("")
    clearSelection()
    cursor = _text.length()
    paste(Nullable(s), programmaticChangeEvents)
  }

  /** @param str If null, "" is used. */
  def setText(str: Nullable[String]): Unit = {
    val s = str.getOrElse("")
    if (s == _text) ()
    else {
      clearSelection()
      val oldText = _text
      _text = ""
      paste(Nullable(s), false)
      if (programmaticChangeEvents) changeText(oldText, _text)
      cursor = 0
    }
  }

  /** @return Never null, might be an empty string. */
  def getText: String = _text

  /** @return True if the text was changed. */
  private[ui] def changeText(oldText: String, newText: String): Boolean =
    if (newText == oldText) false
    else {
      _text = newText
      val changeEvent = Actor.POOLS.obtain[ChangeListener.ChangeEvent]
      val cancelled   = fire(changeEvent)
      if (cancelled) _text = oldText
      Actor.POOLS.free(changeEvent)
      !cancelled
    }

  /** If false, methods that change the text will not fire {@link ChangeEvent}, the event will be fired only when the user changes the text.
    */
  def setProgrammaticChangeEvents(programmaticChangeEvents: Boolean): Unit =
    this.programmaticChangeEvents = programmaticChangeEvents

  def getProgrammaticChangeEvents: Boolean = programmaticChangeEvents

  def getSelectionStart: Int = selectionStart

  def getSelection: String =
    if (hasSelection) _text.substring(Math.min(selectionStart, cursor), Math.max(selectionStart, cursor))
    else ""

  /** Sets the selected text. */
  def setSelection(selectionStart: Int, selectionEnd: Int): Unit = {
    if (selectionStart < 0) throw new IllegalArgumentException("selectionStart must be >= 0")
    if (selectionEnd < 0) throw new IllegalArgumentException("selectionEnd must be >= 0")
    var ss = Math.min(_text.length(), selectionStart)
    var se = Math.min(_text.length(), selectionEnd)
    if (se == ss) {
      clearSelection()
    } else {
      if (se < ss) {
        val temp = se
        se = ss
        ss = temp
      }
      hasSelection = true
      this.selectionStart = ss
      cursor = se
    }
  }

  def selectAll(): Unit =
    setSelection(0, _text.length())

  def clearSelection(): Unit =
    hasSelection = false

  /** Sets the cursor position and clears any selection. */
  def setCursorPosition(cursorPosition: Int): Unit = {
    if (cursorPosition < 0) throw new IllegalArgumentException("cursorPosition must be >= 0")
    clearSelection()
    cursor = Math.min(cursorPosition, _text.length())
  }

  def getCursorPosition: Int = cursor

  /** Default is an instance of {@link DefaultOnscreenKeyboard}. */
  def getOnscreenKeyboard: OnscreenKeyboard = keyboard

  def setOnscreenKeyboard(keyboard: OnscreenKeyboard): Unit =
    this.keyboard = keyboard

  def setClipboard(clipboard: Clipboard): Unit =
    this.clipboard = clipboard

  override def getPrefWidth: Float = 150

  override def getPrefHeight: Float = {
    var topAndBottom = 0f
    var minHeight    = 0f
    _style.background.foreach { bg =>
      topAndBottom = Math.max(topAndBottom, bg.bottomHeight + bg.topHeight)
      minHeight = Math.max(minHeight, bg.minHeight)
    }
    _style.focusedBackground.foreach { fb =>
      topAndBottom = Math.max(topAndBottom, fb.bottomHeight + fb.topHeight)
      minHeight = Math.max(minHeight, fb.minHeight)
    }
    _style.disabledBackground.foreach { db =>
      topAndBottom = Math.max(topAndBottom, db.bottomHeight + db.topHeight)
      minHeight = Math.max(minHeight, db.minHeight)
    }
    Math.max(topAndBottom + textHeight, minHeight)
  }

  /** Sets text horizontal alignment (left, center or right).
    * @see
    *   Align
    */
  def setAlignment(alignment: Align): Unit =
    this.textHAlign = alignment

  def getAlignment: Align = textHAlign

  /** If true, the text in this text field will be shown as bullet characters.
    * @see
    *   #setPasswordCharacter(char)
    */
  def setPasswordMode(passwordMode: Boolean): Unit = {
    this.passwordMode = passwordMode
    updateDisplayText()
  }

  def isPasswordMode: Boolean = passwordMode

  /** Sets the password character for the text field. The character must be present in the {@link BitmapFont}. Default is 149 (bullet).
    */
  def setPasswordCharacter(passwordCharacter: Char): Unit = {
    this.passwordCharacter = passwordCharacter
    if (passwordMode) updateDisplayText()
  }

  def setBlinkTime(blinkTime: Float): Unit =
    this.blinkTime = Seconds(blinkTime)

  def setDisabled(isDisabled: Boolean): Unit =
    this.disabled = isDisabled

  def isDisabled: Boolean = disabled

  protected def moveCursor(forward: Boolean, jump: Boolean): Unit = {
    val limit      = if (forward) _text.length() else 0
    val charOffset = if (forward) 0 else -1
    if (forward) {
      cursor += 1
      while (cursor < limit && jump && continueCursor(cursor, charOffset))
        cursor += 1
    } else {
      cursor -= 1
      while (cursor > limit && jump && continueCursor(cursor, charOffset))
        cursor -= 1
    }
  }

  protected def continueCursor(index: Int, offset: Int): Boolean = {
    val c = _text.charAt(index + offset)
    isWordCharacter(c)
  }

  class KeyRepeatTask extends Timer.Task {
    var keycode: Key = Key(0)

    def run(): Unit =
      if (stage.isEmpty) {
        cancel()
      } else {
        inputListener.keyDown(null, keycode)
      }
  }

  /** Basic input listener for the text field */
  class TextFieldClickListener extends ClickListener {

    override def clicked(event: InputEvent, x: Float, y: Float): Unit = {
      val count = tapCount % 4
      if (count == 0) clearSelection()
      if (count == 2) {
        val array = wordUnderCursor(x)
        setSelection(array(0), array(1))
      }
      if (count == 3) selectAll()
    }

    override def touchDown(event: InputEvent, x: Float, y: Float, pointer: Int, button: Button): Boolean =
      if (!super.touchDown(event, x, y, pointer, button)) false
      else if (pointer == 0 && button != Button(0)) false
      else if (disabled) true
      else {
        setCursorPosition(x, y)
        selectionStart = cursor
        stage.foreach { stage =>
          stage.setKeyboardFocus(Nullable(TextField.this))
        }
        keyboard.show(TextField.this)
        hasSelection = true
        true
      }

    override def touchDragged(event: InputEvent, x: Float, y: Float, pointer: Int): Unit = {
      super.touchDragged(event, x, y, pointer)
      setCursorPosition(x, y)
    }

    override def touchUp(event: InputEvent, x: Float, y: Float, pointer: Int, button: Button): Unit = {
      if (selectionStart == cursor) hasSelection = false
      super.touchUp(event, x, y, pointer, button)
    }

    protected def setCursorPosition(x: Float, y: Float): Unit = {
      cursor = letterUnderCursor(x)

      cursorOn = focused
      blinkTask.cancel()
      if (focused) Timer.schedule(blinkTask, blinkTime, blinkTime)
    }

    protected def goHome(jump: Boolean): Unit =
      cursor = 0

    protected def goEnd(jump: Boolean): Unit =
      cursor = _text.length()

    override def keyDown(event: InputEvent, keycode: Key): Boolean = boundary {
      if (disabled) boundary.break(false)

      cursorOn = focused
      blinkTask.cancel()
      if (focused) Timer.schedule(blinkTask, blinkTime, blinkTime)

      if (!hasKeyboardFocus) boundary.break(false)

      var repeat  = false
      val ctrl    = UIUtils.ctrl()
      val jump    = ctrl && !passwordMode
      var handled = true

      if (ctrl) {
        keycode match {
          case Input.Keys.V =>
            paste(clipboard.contents, true)
            repeat = true
          case Input.Keys.C | Input.Keys.INSERT =>
            copy()
            boundary.break(true)
          case Input.Keys.X =>
            cut(true)
            boundary.break(true)
          case Input.Keys.A =>
            selectAll()
            boundary.break(true)
          case Input.Keys.Z =>
            val oldText = _text
            setText(Nullable(undoText))
            undoText = oldText
            updateDisplayText()
            boundary.break(true)
          case _ =>
            handled = false
        }
      }

      if (UIUtils.shift()) {
        keycode match {
          case Input.Keys.INSERT =>
            paste(clipboard.contents, true)
          case Input.Keys.FORWARD_DEL =>
            cut(true)
          case _ =>
        }

        // selection block
        val temp             = cursor
        var selectionHandled = false
        keycode match {
          case Input.Keys.LEFT =>
            moveCursor(false, jump)
            repeat = true
            handled = true
            selectionHandled = true
          case Input.Keys.RIGHT =>
            moveCursor(true, jump)
            repeat = true
            handled = true
            selectionHandled = true
          case Input.Keys.HOME =>
            goHome(jump)
            handled = true
            selectionHandled = true
          case Input.Keys.END =>
            goEnd(jump)
            handled = true
            selectionHandled = true
          case _ =>
        }
        if (selectionHandled) {
          if (!hasSelection) {
            selectionStart = temp
            hasSelection = true
          }
        }
      } else {
        // Cursor movement or other keys (kills selection).
        keycode match {
          case Input.Keys.LEFT =>
            moveCursor(false, jump)
            clearSelection()
            repeat = true
            handled = true
          case Input.Keys.RIGHT =>
            moveCursor(true, jump)
            clearSelection()
            repeat = true
            handled = true
          case Input.Keys.HOME =>
            goHome(jump)
            clearSelection()
            handled = true
          case Input.Keys.END =>
            goEnd(jump)
            clearSelection()
            handled = true
          case _ =>
        }
      }

      cursor = MathUtils.clamp(cursor, 0, _text.length())

      if (repeat) scheduleKeyRepeatTask(keycode)
      handled
    }

    protected def scheduleKeyRepeatTask(keycode: Key): Unit =
      if (!keyRepeatTask.isScheduled || keyRepeatTask.keycode != keycode) {
        keyRepeatTask.keycode = keycode
        keyRepeatTask.cancel()
        Timer.schedule(keyRepeatTask, keyRepeatInitialTime, keyRepeatTime)
      }

    override def keyUp(event: InputEvent, keycode: Key): Boolean =
      if (disabled) false
      else {
        keyRepeatTask.cancel()
        true
      }

    /** Checks if focus traversal should be triggered. The default implementation uses {@link TextField#focusTraversal} and the typed character, depending on the OS.
      * @param character
      *   The character that triggered a possible focus traversal.
      * @return
      *   true if the focus should change to the {@link TextField#next(boolean) next} input field.
      */
    protected def checkFocusTraversal(character: Char): Boolean =
      focusTraversal && (character == TAB ||
        ((character == CARRIAGE_RETURN || character == NEWLINE) && (UIUtils.isAndroid || UIUtils.isIos)))

    override def keyTyped(event: InputEvent, character: Char): Boolean = boundary {
      if (disabled) boundary.break(false)

      // Disallow "typing" most ASCII control characters, which would show up as a space when onlyFontChars is true.
      character match {
        case BACKSPACE | TAB | NEWLINE | CARRIAGE_RETURN =>
        // allowed
        case _ =>
          if (character < 32) boundary.break(false)
      }

      if (!hasKeyboardFocus) boundary.break(false)

      if (UIUtils.isMac && Sge().input.isKeyPressed(Input.Keys.SYM)) boundary.break(true)

      if (checkFocusTraversal(character)) {
        val transferred = next(UIUtils.shift())
        if (transferred.isEmpty) keyboard.close()
      } else {
        val enter     = character == CARRIAGE_RETURN || character == NEWLINE
        val delete    = character == DELETE
        val backspace = character == BACKSPACE
        val add       = if (enter) writeEnters else !onlyFontChars || _style.font.data.hasGlyph(character)
        val remove    = backspace || delete
        if (add || remove) {
          val oldText   = _text
          val oldCursor = cursor
          if (remove) {
            if (hasSelection)
              cursor = TextField.this.delete(false)
            else {
              if (backspace && cursor > 0) {
                _text = _text.substring(0, cursor - 1) + _text.substring(cursor)
                cursor -= 1
                renderOffset = 0
              }
              if (delete && cursor < _text.length()) {
                _text = _text.substring(0, cursor) + _text.substring(cursor + 1)
              }
            }
          }
          if (add && !remove) {
            // Character may be added to the text.
            if (!enter) {
              var filterReject = false
              filter.foreach { f =>
                if (!f.acceptChar(TextField.this, character)) filterReject = true
              }
              if (filterReject) boundary.break(true)
            }
            if (!withinMaxLength(_text.length() - (if (hasSelection) Math.abs(cursor - selectionStart) else 0)))
              boundary.break(true)
            if (hasSelection) cursor = TextField.this.delete(false)
            val insertion = if (enter) "\n" else String.valueOf(character)
            _text = insert(cursor, insertion, _text)
            cursor += 1
          }
          undoText
          if (changeText(oldText, _text)) {
            val time = System.currentTimeMillis()
            if (time - 750 > lastChangeTime) undoText = oldText
            lastChangeTime = time
            updateDisplayText()
          } else if (_text != oldText) {
            // Keep cursor movement if the text is the same.
            cursor = oldCursor
          }
        }
      }
      listener.foreach(_.keyTyped(TextField.this, character))
      true
    }
  }
}

object TextField {

  private[ui] val BACKSPACE:       Char = 8
  private[ui] val CARRIAGE_RETURN: Char = '\r'
  private[ui] val NEWLINE:         Char = '\n'
  private[ui] val TAB:             Char = '\t'
  private[ui] val DELETE:          Char = 127
  private[ui] val BULLET:          Char = 149

  var DEFAULT_ONSCREEN_KEYBOARD: OnscreenKeyboard = DefaultOnscreenKeyboard()

  private val tmp1: Vector2 = Vector2()
  private val tmp2: Vector2 = Vector2()
  private val tmp3: Vector2 = Vector2()

  var keyRepeatInitialTime: Seconds = Seconds(0.4f)
  var keyRepeatTime:        Seconds = Seconds(0.1f)

  /** Interface for listening to typed characters.
