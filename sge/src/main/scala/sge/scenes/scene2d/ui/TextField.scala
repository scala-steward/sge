/** A single-line text input field. <p> The preferred height of a text field is the height of the {@link TextFieldStyle#font} and {@link TextFieldStyle#background}. The preferred width of a text field
  * is 150, a relatively arbitrary size. <p> The text field will copy the currently selected text when ctrl+c is pressed, and paste any text in the clipboard when ctrl+v is pressed. Clipboard
  * functionality is provided via the {@link Clipboard} interface. Currently there are two standard implementations, one for the desktop and one for Android. The Android clipboard is a stub, as copy &
  * pasting on Android is not supported yet. <p> The text field allows you to specify an {@link OnscreenKeyboard} for displaying a softkeyboard and piping all key events generated by the keyboard to
  * the text field. There are two standard implementations, one for the desktop and one for Android. The desktop keyboard is a stub, as a softkeyboard is not needed on the desktop. The Android
  * {@link OnscreenKeyboard} implementation will bring up the default IME.
  * @author
  *   mzechner
  * @author
  *   Nathan Sweet
  */
class TextField(text: Nullable[String], style: TextField.TextFieldStyle)(using Sge) extends Widget with Disableable with Styleable[TextField.TextFieldStyle] {

