  var disabled:               Boolean                     = false
  private var textHAlign:     Align                       = Align.left
  private var selectionX:     Float                       = 0
  private var selectionWidth: Float                       = 0

  var undoText:       String = ""
  var lastChangeTime: Long   = 0

