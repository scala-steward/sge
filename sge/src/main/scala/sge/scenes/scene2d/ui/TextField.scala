  def this(text: Nullable[String], skin: Skin, styleName: String)(using Sge) = this(text, skin.get[TextField.TextFieldStyle](styleName))

  protected def initialize(): Unit = {
    inputListener = createInputListener()
    addListener(inputListener)
  }

  protected def createInputListener(): InputListener =
    TextFieldClickListener()

  protected def letterUnderCursor(x: Float): Int = boundary {
    var adjustedX  = x - textOffset - fontOffset + _style.font.data.cursorX + glyphPositions(visibleTextStart)
    val background = getBackgroundDrawable()
    background.foreach { bg =>
      adjustedX -= bg.leftWidth
    }
    val n = this.glyphPositions.size
    var i = 1
    while (i < n) {
      if (glyphPositions(i) > adjustedX) {
        if (glyphPositions(i) - adjustedX <= adjustedX - glyphPositions(i - 1)) boundary.break(i)
        else boundary.break(i - 1)
      }
      i += 1
    }
    n - 1
  }

  protected def isWordCharacter(c: Char): Boolean =
    Character.isLetterOrDigit(c)

  protected def wordUnderCursor(at: Int): Array[Int] = {
    val text  = this._text
    var right = text.length()
    var left  = 0
    var index = at
    if (at >= text.length()) {
      left = text.length()
      right = 0
    } else {
      while (index < right)
        if (!isWordCharacter(text.charAt(index))) {
          right = index
          index = right // break
        } else {
          index += 1
        }
      index = at - 1
      while (index > -1)
        if (!isWordCharacter(text.charAt(index))) {
          left = index + 1
          index = -1 // break
        } else {
          index -= 1
        }
    }
    Array(left, right)
  }

  private[ui] def wordUnderCursor(x: Float): Array[Int] =
    wordUnderCursor(letterUnderCursor(x))

  private[ui] def withinMaxLength(size: Int): Boolean =
    maxLength <= 0 || size < maxLength

  def setMaxLength(maxLength: Int): Unit =
    this.maxLength = maxLength

  def getMaxLength: Int = this.maxLength

  /** A list of possible strings that are valid to autocomplete. Currently only has an effect on mobile with NativeOnscreenKeyboard
