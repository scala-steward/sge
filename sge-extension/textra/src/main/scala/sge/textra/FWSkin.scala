  /** For each LabelStyle entry, creates a corresponding Styles.LabelStyle and adds it to the skin. */
  private def loadTextraLabelStyles(typeValue: Json): Unit =
    typeValue match {
      case Json.Obj(entries) =>
        entries.fields.foreach { case (entryName, jsonData) =>
          val s2d      = get(entryName, classOf[Label.LabelStyle])
          val fontName = getStringField(jsonData, "font", "default-font")
          val fontOpt  = optional(fontName, classOf[Font])
          fontOpt.foreach { font =>
            val stt = new Styles.LabelStyle(font, s2d.fontColor)
            stt.background = s2d.background.map(_.asInstanceOf[AnyRef])
            add(entryName, stt, classOf[Styles.LabelStyle])
          }
        }
      case _ => ()
    }

