            val lsOpt         = optional(listStyleName, classOf[Styles.ListStyle])

            lsOpt.foreach { ls =>
              val stt = new Styles.SelectBoxStyle(font, s2d.fontColor, s2d.background.map(_.asInstanceOf[AnyRef]), Nullable(s2d.scrollStyle.asInstanceOf[AnyRef]), ls)
              stt.background = s2d.background.map(_.asInstanceOf[AnyRef])
              stt.backgroundDisabled = s2d.backgroundDisabled.map(_.asInstanceOf[AnyRef])
              stt.backgroundOpen = s2d.backgroundOpen.map(_.asInstanceOf[AnyRef])
              stt.backgroundOver = s2d.backgroundOver.map(_.asInstanceOf[AnyRef])
              s2d.disabledFontColor.foreach(c => stt.disabledFontColor = Nullable(new Color(c)))
              s2d.overFontColor.foreach(c => stt.overFontColor = Nullable(new Color(c)))
              add(entryName, stt, classOf[Styles.SelectBoxStyle])
            }
          }
        }
      case _ => ()
    }

  /** For each TextFieldStyle entry, creates a corresponding Styles.TextFieldStyle. */
  private def loadTextraTextFieldStyles(typeValue: Json): Unit =
    typeValue match {
      case Json.Obj(entries) =>
        entries.fields.foreach { case (entryName, jsonData) =>
          val s2d      = get(entryName, classOf[TextField.TextFieldStyle])
          val fontName = getStringField(jsonData, "font", "default-font")
          val fontOpt  = optional(fontName, classOf[Font])
          fontOpt.foreach { font =>
            val stt = new Styles.TextFieldStyle(
              font,
              s2d.fontColor,
              s2d.cursor.map(_.asInstanceOf[AnyRef]),
              s2d.selection.map(_.asInstanceOf[AnyRef]),
              s2d.background.map(_.asInstanceOf[AnyRef])
            )
            s2d.messageFontColor.foreach(c => stt.messageFontColor = Nullable(new Color(c)))
            s2d.focusedFontColor.foreach(c => stt.focusedFontColor = Nullable(new Color(c)))
            s2d.disabledFontColor.foreach(c => stt.disabledFontColor = Nullable(new Color(c)))
            stt.focusedBackground = s2d.focusedBackground.map(_.asInstanceOf[AnyRef])
            stt.disabledBackground = s2d.disabledBackground.map(_.asInstanceOf[AnyRef])
            add(entryName, stt, classOf[Styles.TextFieldStyle])
          }
        }
      case _ => ()
    }

  // ---------------------------------------------------------------------------
  // Distance field resizing
  // ---------------------------------------------------------------------------

  /** Calls Font.resizeDistanceField(float, float) with the given width and height on every Font loaded by this skin. If you are mainly loading Font values via a skin, this can be called instead of
    * manually calling resizeDistanceField on each Font, and should be called in ApplicationListener.resize(int, int) (with width and height matching its parameters). Like Font's
    * resizeDistanceField(), this only has an effect on distance field fonts -- those that use SDF, MSDF, or SDF_OUTLINE for their getDistanceField.
    *
    * @param width
    *   should match the width in ApplicationListener.resize(int, int)
    * @param height
    *   should match the height in ApplicationListener.resize(int, int)
    */
  def resizeDistanceFields(width: Float, height: Float): Unit = {
    val allFonts = getAll(classOf[Font])
    allFonts.foreach { fonts =>
      fonts.values.foreach { font =>
        font.resizeDistanceField(width, height)
      }
    }
  }

  /** Calls Font.resizeDistanceField(float, float) with the given width and height on every Font loaded by this skin. This overload takes a Viewport, which helps inform the calculation to make the
    * edges the correct crispness. This does not change the given Viewport.
    *
    * @param width
    *   should match the width in ApplicationListener.resize(int, int)
    * @param height
    *   should match the height in ApplicationListener.resize(int, int)
    * @param viewport
    *   the viewport used for rendering; passed through to Font for crispness calculation
    */
  def resizeDistanceFields(width: Float, height: Float, viewport: Viewport): Unit = {
    val allFonts = getAll(classOf[Font])
    allFonts.foreach { fonts =>
      fonts.values.foreach { font =>
        font.resizeDistanceField(width, height, viewport)
      }
    }
  }

  // ---------------------------------------------------------------------------
  // JSON field access helpers (local to FWSkin since Skin's are private)
  // ---------------------------------------------------------------------------

  private def getField(json: Json, name: String): Option[Json] = json match {
    case Json.Obj(obj) =>
      var result: Option[Json] = None
      obj.fields.foreach { case (k, v) => if (k == name) result = Some(v) }
      result
    case _ => None
  }

  private def getStringField(json: Json, name: String, default: String): String =
    getField(json, name) match {
      case Some(Json.Str(s)) => s
      case _                 => default
    }

  private def getFloatField(json: Json, name: String, default: Float): Float =
    getField(json, name) match {
      case Some(Json.Num(n)) => n.toDouble.map(_.toFloat).getOrElse(default)
      case _                 => default
    }

  private def getBoolField(json: Json, name: String, default: Boolean): Boolean =
    getField(json, name) match {
      case Some(Json.Bool(b)) => b
      case _                  => default
    }
}
