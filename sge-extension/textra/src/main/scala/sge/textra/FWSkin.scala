/** A subclass of Skin that includes a serializer for Structured JSON Fonts, which are typically generated by fontwriter (hence the "FW" in the name). This can load FontWriter's JSON format into both
  * Font and BitmapFont objects, from the same files. These files can have a variety of extensions depending on if or how they are compressed: .json, .dat, .ubj, .json.lzma, or .ubj.lzma; .json.lzma
  * is currently preferred.
  *
  * This class is also important when using the styles in Styles, since it allows reading in a skin JSON's styles both as the scene2d.ui format and as styles for TextraTypist widgets.
  */
class FWSkin()(using Sge) extends Skin() {

  /** Creates a skin containing the resources in the specified skin JSON file. If a file in the same directory with a ".atlas" extension exists, it is loaded as a TextureAtlas and the texture regions
    * added to the skin. The atlas is automatically disposed when the skin is disposed.
    * @param skinFile
    *   The JSON file to be read.
    */
  def this(skinFile: FileHandle)(using Sge) = {
    this()
    val atlasFile = skinFile.sibling(skinFile.nameWithoutExtension + ".atlas")
    if (atlasFile.exists()) {
      val atlas = TextureAtlas(atlasFile)
      addRegions(atlas)
    }
    load(skinFile)
  }

  /** Creates a skin containing the resources in the specified skin JSON file and the texture regions from the specified atlas. The atlas is automatically disposed when the skin is disposed.
    * @param skinFile
    *   The JSON file to be read.
    * @param atlas
    *   The texture atlas to be associated with the Skin.
    */
  def this(skinFile: FileHandle, atlas: TextureAtlas)(using Sge) = {
    this()
    addRegions(atlas)
    load(skinFile)
  }

  /** Creates a skin containing the texture regions from the specified atlas. The atlas is automatically disposed when the skin is disposed.
    * @param atlas
    *   The texture atlas to be associated with the Skin.
    */
  def this(atlas: TextureAtlas)(using Sge) = {
    this()
    addRegions(atlas)
  }

  /** Overrides load to first perform standard skin loading, then re-reads the skin JSON to create Font objects from BitmapFont entries and TextraTypist styles from scene2d widget style entries.
    */
  override def load(skinFile: FileHandle): Unit = {
    // First, do the standard skin loading (BitmapFont, Color, widget styles, etc.)
    super.load(skinFile)

    // Second pass: re-read the JSON to create Font and TextraTypist style objects.
    try {
      val root = skinFile.readJson[Json]
      root match {
        case Json.Obj(rootObj) =>
          rootObj.fields.foreach { case (typeName, typeValue) =>
            typeName match {
              case "Font" | "com.github.tommyettinger.textra.Font" =>
                loadFontsFromFontSection(typeValue, skinFile)
              case "BitmapFont" | "com.badlogic.gdx.graphics.g2d.BitmapFont" =>
                loadFontsFromBitmapFontSection(typeValue, skinFile)
              case "LabelStyle" | "com.badlogic.gdx.scenes.scene2d.ui.Label$LabelStyle" =>
                loadTextraLabelStyles(typeValue)
              case "TextButtonStyle" | "com.badlogic.gdx.scenes.scene2d.ui.TextButton$TextButtonStyle" =>
                loadTextraTextButtonStyles(typeValue)
              case "ImageTextButtonStyle" | "com.badlogic.gdx.scenes.scene2d.ui.ImageTextButton$ImageTextButtonStyle" =>
                loadTextraImageTextButtonStyles(typeValue)
              case "CheckBoxStyle" | "com.badlogic.gdx.scenes.scene2d.ui.CheckBox$CheckBoxStyle" =>
                loadTextraCheckBoxStyles(typeValue)
              case "WindowStyle" | "com.badlogic.gdx.scenes.scene2d.ui.Window$WindowStyle" =>
                loadTextraWindowStyles(typeValue)
              case "TextTooltipStyle" | "com.badlogic.gdx.scenes.scene2d.ui.TextTooltip$TextTooltipStyle" =>
                loadTextraTextTooltipStyles(typeValue)
              case "ListStyle" | "com.badlogic.gdx.scenes.scene2d.ui.List$ListStyle" =>
                loadTextraListStyles(typeValue)
              case "SelectBoxStyle" | "com.badlogic.gdx.scenes.scene2d.ui.SelectBox$SelectBoxStyle" =>
                loadTextraSelectBoxStyles(typeValue)
              case "TextFieldStyle" | "com.badlogic.gdx.scenes.scene2d.ui.TextField$TextFieldStyle" =>
                loadTextraTextFieldStyles(typeValue)
              case _ => () // handled by super.load
            }
          }
        case _ => ()
      }
    } catch {
      case ex: SgeError  => throw ex
      case ex: Exception =>
        throw SgeError.InvalidInput("Error reading FWSkin file: " + skinFile + ": " + ex.getMessage)
    }
  }

  // ---------------------------------------------------------------------------
  // Font loading from BitmapFont JSON entries
  // ---------------------------------------------------------------------------

  /** Reads standalone Font entries from the skin JSON and creates corresponding Font objects directly. Unlike BitmapFont entries which create both a BitmapFont and a Font, these entries only create
    * Font objects.
    */
  private def loadFontsFromFontSection(typeValue: Json, skinFile: FileHandle): Unit =
    typeValue match {
      case Json.Obj(entries) =>
        entries.fields.foreach { case (entryName, jsonData) =>
          try {
            val font = readFont(jsonData, skinFile)
            add(entryName, font, classOf[Font])
          } catch {
            case ex: RuntimeException =>
              throw SgeError.InvalidInput("Error loading Font entry: " + entryName + ": " + ex.getMessage)
          }
        }
      case _ => ()
    }

  /** Reads BitmapFont entries from the skin JSON and creates corresponding Font objects. Each BitmapFont entry that references a Structured JSON font file (.json, .dat, .ubj, .json.lzma, .ubj.lzma)
    * gets a Font created alongside the BitmapFont that super.load() already stored. For .fnt files, a Font is also created using the FNT data.
    */
  private def loadFontsFromBitmapFontSection(typeValue: Json, skinFile: FileHandle): Unit =
    typeValue match {
      case Json.Obj(entries) =>
        entries.fields.foreach { case (entryName, jsonData) =>
          try {
            val font = readFont(jsonData, skinFile)
            add(entryName, font, classOf[Font])
          } catch {
            case ex: RuntimeException =>
              throw SgeError.InvalidInput("Error loading Font from BitmapFont entry: " + entryName + ": " + ex.getMessage)
          }
        }
      case _ => ()
    }

  /** Creates a Font from BitmapFont JSON data, handling both Structured JSON fonts (.json, .dat, .ubj, .json.lzma, .ubj.lzma) and .fnt files.
    */
  private def readFont(jsonData: Json, skinFile: FileHandle): Font = {
    val path = getStringField(jsonData, "file", "")

    var fontFile = skinFile.sibling(path)
    if (!fontFile.exists()) fontFile = Sge().files.internal(path)
    if (!fontFile.exists()) throw SgeError.InvalidInput("Font file not found: " + fontFile)

    val fontPath = fontFile.path

    val lzb    = "dat".equalsIgnoreCase(fontFile.extension)
    val js     = "json".equalsIgnoreCase(fontFile.extension)
    val ubj    = "ubj".equalsIgnoreCase(fontFile.extension)
    val jslzma = fontFile.name.length > 10 && ".json.lzma".equalsIgnoreCase(fontFile.name.substring(fontFile.name.length - 10))
    val ublzma = fontFile.name.length > 9 && ".ubj.lzma".equalsIgnoreCase(fontFile.name.substring(fontFile.name.length - 9))
    val fw     = lzb || js || ubj || jslzma || ublzma

    val scaledSize = getFloatField(jsonData, "scaledSize", -1f)
    val xAdj       = getFloatField(jsonData, "xAdjust", 0f)
    val yAdj       = getFloatField(jsonData, "yAdjust", 0f)
    val wAdj       = getFloatField(jsonData, "widthAdjust", 0f)
    val hAdj       = getFloatField(jsonData, "heightAdjust", 0f)
    // This defaults to false, which is not what Skin normally defaults to.
    // You can set it to true if you expect a BitmapFont to be used at pixel-perfect 100% zoom only.
    val useIntegerPositions = getBoolField(jsonData, "useIntegerPositions", false)
    // This defaults to true, because anything FontWriter produces is compatible with makeGridGlyphs.
    val makeGridGlyphs = getBoolField(jsonData, "makeGridGlyphs", true)

    // Use a region with the same name as the font, else use a PNG file in the same directory as the font file.
    val nameStart  = Math.max(fontPath.lastIndexOf('/'), fontPath.lastIndexOf('\\')) + 1
    val regionName = fontPath.substring(nameStart, Math.max(0, fontPath.indexOf('.', nameStart)))
    try {
      val font    = new Font()
      val regions = getRegions(regionName)
      if (regions.isDefined) {
        val firstRegion = regions.get.apply(0)
        if (fw) {
          font.loadJSON(fontFile, firstRegion, xAdj, yAdj, wAdj, hAdj, makeGridGlyphs)
        } else {
          font.loadFNT(fontFile, xAdj, yAdj, wAdj, hAdj, makeGridGlyphs)
        }
      } else {
        val region = optional(regionName, classOf[TextureRegion])
        if (region.isDefined) {
          if (fw) {
            font.loadJSON(fontFile, region.get, xAdj, yAdj, wAdj, hAdj, makeGridGlyphs)
          } else {
            font.loadFNT(fontFile, xAdj, yAdj, wAdj, hAdj, makeGridGlyphs)
          }
        } else {
          val imageFile = Sge().files.internal(fontPath).sibling(regionName + ".png")
          if (imageFile.exists()) {
            val texRegion = TextureRegion(Texture(imageFile))
            if (fw) {
              font.loadJSON(fontFile, texRegion, xAdj, yAdj, wAdj, hAdj, makeGridGlyphs)
            } else {
              font.loadFNT(fontFile, xAdj, yAdj, wAdj, hAdj, makeGridGlyphs)
            }
          } else {
            if (fw) {
              throw RuntimeException("Missing image file or TextureRegion.")
            } else {
              font.loadFNT(fontFile, xAdj, yAdj, wAdj, hAdj, makeGridGlyphs)
            }
          }
        }
      }
      font.useIntegerPositions(useIntegerPositions)
      // Scaled size is the desired line height to scale the font to.
      // This is different from with BitmapFont, which has a desired cap height.
      if (scaledSize != -1) font.scaleHeightTo(scaledSize)
      font
    } catch {
      case ex: RuntimeException =>
        throw SgeError.InvalidInput("Error loading Font: " + fontPath + ": " + ex.getMessage)
    }
  }

